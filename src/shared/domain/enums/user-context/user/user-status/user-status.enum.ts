/**
 * UserStatusEnum represents the status of a user.
 * ACTIVE: The user is active and can login to the system.
 * INACTIVE: The user is inactive and cannot login to the system.
 * BLOCKED: The user is blocked and cannot login to the system.
 */
export enum UserStatusEnum {
  ACTIVE = 'ACTIVE',
  INACTIVE = 'INACTIVE',
  BLOCKED = 'BLOCKED',
}
